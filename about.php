                                    <h2>Adaptive Reuse of Riverside Warehouse</h2>
                                    <div class="project-meta">
                                        <ul class="project-metadata">
                                            <li><strong>Client:</strong> Riverside Developments</li>
                                            <li><strong>Location:</strong> Colombo, Sri Lanka</li>
                                            <li><strong>Year:</strong> 2024</li>
                                            <li><strong>Area:</strong> 2,400 m<sup>2</sup></li>
                                        </ul>
                                    </div>

                                    <p class="project-summary">This project transforms a disused riverside warehouse into a mixed-use creative hub that includes galleries, studio spaces and a public courtyard. The design strategy prioritises retaining the building's industrial character while introducing modern interventions that improve daylighting, passive ventilation and public accessibility.</p>

                                    <h3>Design Intent</h3>
                                    <p>Key design moves include splitting the long plan with a lightwell to introduce natural daylight deep into the floorplate, exposing structural elements to celebrate the building's history, and creating flexible interiors that can be adapted by occupants over time. Material choices focus on locally sourced timber and fair-faced concrete to give a warm yet robust palette.</p>

                                    <h3>Scope & Services</h3>
                                    <ul>
                                        <li>Architectural concept, detailed design and contract administration</li>
                                        <li>Interior design and furniture layout</li>
                                        <li>Landscape design for the public courtyard</li>
                                        <li>Coordination with structural and MEP consultants</li>
                                    </ul>

                                    <p class="project-summary">The project was delivered through close collaboration with the client and engineers, and included a phased handover to allow early occupation of gallery spaces while fit-out continued in other wings. The adaptive reuse approach conserved embodied carbon and reduced construction waste compared with a full demolition and rebuild.</p>

